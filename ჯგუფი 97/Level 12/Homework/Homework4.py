
def square_number(num):
    return num ** 2


def average_of_three(a, b, c):
    return (a + b + c) / 3

def is_positive(num):
    return num > 0


def double_number(num):
    return num * 2


# გამოყენების მაგალითები
print("5-ის კვადრატი:", square_number(5))             
print("სამი რიცხვის საშუალო:", average_of_three(3, 6, 9))  
print("დადებითია?", is_positive(5))                   
print("დადებითია?", is_positive(-3))                  
print("5-ის გაორმაგება:", double_number(5))            
